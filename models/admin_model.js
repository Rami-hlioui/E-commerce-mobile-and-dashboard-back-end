const mongoose = require("mongoose");

const AdminSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  verified: { type: Boolean, default: false },
});

const Admin = mongoose.model("Admin", AdminSchema);

module.exports = Admin;
